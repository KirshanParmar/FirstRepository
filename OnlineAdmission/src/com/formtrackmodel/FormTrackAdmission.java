package com.formtrackmodel;

public class FormTrackAdmission {
	
	private int id;
	private String apply;
	
	
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getApply() {
		return apply;
	}

	public void setApply(String apply) {
		this.apply = apply;
	}

	public String toString() {
		return apply.toString();
	}

	
}
